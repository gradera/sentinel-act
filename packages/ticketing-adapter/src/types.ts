// ---------------------------------------------------------------------------
// §4.1 The swappable adapter port
// ---------------------------------------------------------------------------

export type TicketPriority = "P1_urgent" | "P2_high" | "P3_normal";

export interface TicketAssignee {
  /** Resolved queue/user identifier in the external system — a Jira
   *  account id, a ServiceNow assignment-group sys_id, or (for the
   *  generic webhook default) an opaque string the receiving system
   *  interprets itself. */
  externalAssigneeRef: string;
  /** Human-readable label, logging/debugging only — never used for
   *  lookup or comparison. */
  displayLabel: string;
  /** true when RoleAssigneeMapPort had no entry for owner_role and the
   *  configured fallback queue was used instead — see FR-8. */
  isFallback: boolean;
}

export interface CreateTicketRequest {
  /** Idempotency/dedupe key for the *external* system, where supported
   *  (e.g. Jira's "external ID" custom field). Always exactly
   *  ProcessTask.task_id — see §6 Idempotency. Concrete adapters that
   *  cannot enforce dedupe server-side (the generic webhook) rely on this
   *  unit's own outbox + TicketMapping dedupe instead (§6); this field is
   *  still passed through so a smarter receiver can use it too. */
  dedupeKey: string;
  title: string;
  description: string; // markdown; composition rules in FR-7
  assignee: TicketAssignee;
  dueDate: string; // ISO datetime
  priority: TicketPriority;
  labels: string[];
  sourceRefs: {
    obligation_id: string;
    task_id: string;
    circular_id: string | null;
    clause_para_ref: string | null;
  };
}

export interface CreateTicketResult {
  externalTicketId: string;
  externalTicketUrl: string | null;
  raw: unknown; // adapter-specific raw response, stored for debugging only
}

export interface UpdateTicketFields {
  title?: string;
  description?: string;
  assignee?: TicketAssignee;
  dueDate?: string;
  priority?: TicketPriority;
  /** Appended as a ticket comment rather than a field overwrite — the
   *  only update shape this spec's v1 scope actually anticipates a future
   *  caller using (e.g. "this obligation was superseded", §13 item 6). */
  comment?: string;
}

export interface UpdateTicketRequest {
  externalTicketId: string;
  fields: UpdateTicketFields;
}

export interface UpdateTicketResult {
  updated: boolean;
  raw: unknown;
}

/** The swappable port. Jira, ServiceNow, and the generic webhook
 *  reference implementation are all just implementations of this
 *  interface — nothing in the trigger/mapping/idempotency/outbox layer
 *  below (§4.2–§4.4) knows or cares which concrete adapter is wired in.
 *  Exactly one adapter is active per deployment (no fan-out to multiple
 *  ticketing systems in this spec's scope). */
export interface TicketingAdapter {
  readonly adapterName: string; // e.g. "generic-webhook", "jira", "servicenow"
  createTicket(request: CreateTicketRequest): Promise<CreateTicketResult>;
  updateTicket(request: UpdateTicketRequest): Promise<UpdateTicketResult>;
}

// ---------------------------------------------------------------------------
// §4.2 The role -> assignee/queue lookup (open dependency)
// ---------------------------------------------------------------------------

/** Resolves ProcessTask.owner_role (e.g. "Compliance Officer",
 *  "Stockbroker Operations Team") to a real assignee or queue in the
 *  external ticketing system. NO CONCRETE DATA SOURCE FOR THIS EXISTS
 *  YET anywhere in the repo — there is no roster/directory service, no
 *  role-to-user table in the graph schema (IntermediaryCategory is a
 *  regulatory taxonomy, not a staffing directory), and no HR/identity
 *  integration. This is an explicit, flagged open dependency (§13 item 1),
 *  not something this spec invents a fake answer for. */
export interface RoleAssigneeMapPort {
  resolve(owner_role: string): Promise<TicketAssignee | null>; // null = no mapping found
}

// ---------------------------------------------------------------------------
// §4.3 Trigger event (forward-declared additive hook into Spec 08)
// ---------------------------------------------------------------------------

/** Emitted by Spec 08's finalizeCommitStep (FR-27 Tier A path, FR-28
 *  Tier B/C-approved path) immediately after GraphWriter.commitProposal
 *  succeeds — never for a reject or disagreement/escalate outcome
 *  (FR-30/FR-31 there). */
export interface ObligationCommittedEvent {
  event_id: string; // uuid v4, generated by the Orchestrator at emit time
  obligation_id: string;
  task_id: string;
  /** Exactly one of these two — the only ObligationStatus values this
   *  event ever fires for (see §1). */
  final_status: "tier_a_committed" | "committed";
  tier: "A" | "B" | "C" | "ESCALATE";
  committed_at: string; // ISO datetime, matches the commit's recorded_at
}

// ---------------------------------------------------------------------------
// §4.4 Outbox and ticket-mapping (idempotency + async retry state)
// ---------------------------------------------------------------------------

export type OutboxStatus = "pending" | "processing" | "succeeded" | "failed_retryable" | "failed_permanent";

export interface TicketingOutboxEntry {
  id: string; // uuid v4, PK
  event_id: string; // UNIQUE — dedupes a redelivered ObligationCommittedEvent, FR-3
  obligation_id: string;
  task_id: string;
  /** DEVIATION FROM SPEC TEXT (documented, not silent): §4.4's literal
   *  TicketingOutboxEntry shape omits `tier`, yet `buildCreateTicketRequest`
   *  (§5.1) requires the full `ObligationCommittedEvent` — specifically
   *  `event.tier`, consumed by FR-12's label composition. The outbox
   *  worker (processOutboxOnce) only has the persisted outbox row when it
   *  runs, often long after the original in-memory event is gone — an
   *  inherent property of the outbox pattern (the row must carry every
   *  field needed to complete the operation later). `tier` cannot be
   *  reliably re-derived from `Obligation.status` alone at that point,
   *  because both Tier B and Tier C collapse to the same `"committed"`
   *  status once approved. Persisting `event.tier` on the row at insert
   *  time (FR-3) is the minimal fix. */
  tier: "A" | "B" | "C" | "ESCALATE";
  status: OutboxStatus;
  attempts: number;
  next_attempt_at: string; // ISO datetime
  last_error: string | null;
  created_at: string;
  updated_at: string;
}

/** One row per ticket ever created. task_id is the PK: v1 creates at
 *  most one ticket per ProcessTask, ever — this table IS the durable
 *  idempotency store the createTicket dedupe check (FR-4) reads before
 *  calling the adapter. */
export interface TicketMapping {
  task_id: string; // PK
  adapter_name: string;
  external_ticket_id: string;
  external_ticket_url: string | null;
  created_at: string;
}

