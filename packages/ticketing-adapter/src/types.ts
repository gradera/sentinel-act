/** buildCreateTicketRequest's data source — read-only, mirrors Spec 09's
 *  item-detail query shape.
 *
 *  DEVIATION FROM SPEC TEXT (documented, not silent): §4.6's literal query
 *  omits `circ.circular_id`, yet `CreateTicketRequest.sourceRefs.
 *  circular_id` (§4.1) requires a value. Adding `circ.circular_id AS
 *  circularId` to the RETURN clause (and a matching `circularId` field on
 *  the `lineage` parameter of `buildCreateTicketRequest`) is the minimal
 *  fix — everything else about the query is verbatim from §4.6. */
export const BUILD_TICKET_LINEAGE_CYPHER = `
MATCH (o:Obligation {obligation_id: $obligationId})-[:MAPPED_TO]->(t:ProcessTask {task_id: $taskId})
OPTIONAL MATCH (o)-[:DERIVED_FROM]->(c:Clause)-[:PART_OF]->(circ:Circular)
RETURN o, t, c.para_ref AS clauseParaRef, circ.title AS circularTitle, circ.date_effective AS circularDateEffective, circ.circular_id AS circularId
`;

/** buildCreateTicketRequest's lineage parameter shape — §4.6's 3 named
 *  fields plus `circularId` (see the deviation note on
 *  BUILD_TICKET_LINEAGE_CYPHER above). */
export interface TicketLineage {
  clauseParaRef: string | null;
  circularTitle: string | null;
  circularDateEffective: string | null;
  circularId: string | null;
}
